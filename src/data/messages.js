export const categoryKeys = Object.keys(messages);

/**
 * Devuelve todos los mensajes en un array plano con su categoría.
 */
export function getAllMessages() {
  const all = [];
  for (const key of categoryKeys) {
    for (const m of messages[key]) {
      all.push({ ...m, category: key });
    }
  }
  return all;
}

/**
 * Busca un mensaje por su id (dentro de cualquier categoría).
 */
export function findMessageById(id) {
  for (const key of categoryKeys) {
    const found = messages[key].find((m) => m.id === id);
    if (found) return { ...found, category: key };
  }
  return null;
}
